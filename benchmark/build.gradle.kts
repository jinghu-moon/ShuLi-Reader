plugins {
    alias(libs.plugins.android.library)
    alias(libs.plugins.kotlin.android)
}

import org.gradle.api.DefaultTask
import org.gradle.api.file.RegularFileProperty
import org.gradle.api.tasks.OutputFile
import org.gradle.api.tasks.TaskAction

android {
    namespace = "com.shuli.reader.benchmark"
    compileSdk = 35

    compileOptions {
        sourceCompatibility = JavaVersion.VERSION_17
        targetCompatibility = JavaVersion.VERSION_17
    }

    kotlinOptions {
        jvmTarget = "17"
    }

    defaultConfig {
        minSdk = 31
        testInstrumentationRunner = "androidx.test.runner.AndroidJUnitRunner"
    }
}

dependencies {
    implementation(libs.androidx.test.ext.junit)
    implementation(libs.androidx.test.espresso.core)
    implementation(libs.androidx.benchmark.macro.junit4)
}

abstract class BenchmarkBaselineReportTask : DefaultTask() {
    @get:OutputFile
    abstract val outputFile: RegularFileProperty

    @TaskAction
    fun writeReport() {
        val report = outputFile.get().asFile
        report.parentFile.mkdirs()
        report.writeText(
            """
            # ShuLi Reader Benchmark Baseline

            This build artifact records the benchmark entry points that are available from the
            public app surface. Numeric metrics are produced by `connectedDebugAndroidTest` on a
            connected device or emulator.

            ## Verified Build-Time Entry Points

            - `:benchmark:assembleDebug`
            - `:benchmark:connectedDebugAndroidTest`

            ## Device Benchmark Scenarios

            | Scenario | Status | Evidence |
            |------|------|------|
            | Cold startup | Instrumented benchmark defined | `StartupMacrobenchmark.coldStartup` |
            | Warm startup | Instrumented benchmark defined | `StartupMacrobenchmark.warmStartup` |
            | Hot startup | Instrumented benchmark defined | `StartupMacrobenchmark.hotStartup` |
            | Reader first frame | Pending seeded library/deep link fixture | Requires device data fixture |
            | Continuous page turning | Pending seeded readable book fixture | Requires device data fixture |
            | Directory scrolling | Pending reader UI directory fixture | Requires device data fixture |
            | Large metadata import | Covered by JVM parser tests; device benchmark pending | `TxtParserLargeFileTest` |

            ## Output Location

            Connected benchmark JSON output is generated by AndroidX Benchmark under the module
            build outputs on the executing device/test run. This Markdown file is generated during
            `assembleDebug` so CI can detect whether the benchmark module is wired.
            """.trimIndent(),
        )
    }
}

val generateBenchmarkBaselineReport by tasks.registering(BenchmarkBaselineReportTask::class) {
    outputFile.set(layout.buildDirectory.file("reports/benchmark/baseline.md"))
}

tasks.matching { it.name == "assembleDebug" }.configureEach {
    finalizedBy(generateBenchmarkBaselineReport)
}
